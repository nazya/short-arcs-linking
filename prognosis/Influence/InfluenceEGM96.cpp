	//==============================================================================//
	//        .
	//  x      .
	// Re - earht equatorial radius
	//==============================================================================//
	void InfluenceForce::GetF_Harm_egm96( double *x_b, int n_harm, double *f_harm )
	{
		double eps = 2.0E-12;
		int n, m, n1, n21, pos_mm, pos_nm1, pos_nm;
		double x[3];
		double ro, rv, w1, w2, w3, w4, dw2, dw3, dw;
		double cosL, sinL, cosmL, sinmL, cosm1L, sinm1L;
		double R, R_n, R_m, tmp, P_nm, P_n1m, P_mm, PR;

		ro = x_b[0]*x_b[0]+x_b[1]*x_b[1];
		rv = sqrt(x_b[2]*x_b[2]+ro);

		x[0] = x_b[0]/rv;
		x[1] = x_b[1]/rv;
		x[2] = x_b[2]/rv;

		f_harm[0] = 0.0;
		f_harm[0] = 0.0;
		f_harm[0] = 0.0;

		ro = sqrt(ro);
		if(ro > eps)
		{
			cosL = x_b[0]/ro;
			sinL = x_b[1]/ro;
		}
		else
		{
			cosL = 1.0;
			sinL = 0.0;
		}

		ro = ro/rv;
		R  = Re/rv;
		w1 = 0.0;
		w2 = 0.0;
		w3 = 0.0;
		w4 = 0.0;
		//  cos(ml), sin(ml), cos((m-1)l), sin((m-1)l)    m = 1
		cosm1L = 1.0;
		sinm1L = 0.0;
		cosmL  = cosL;
		sinmL  = sinL;
		//     egm96(pos_nm) = C_nm, egm96(pos_nm+1) = S_nm
		//   S_n0 = 0,     .

		//############    egm96,
		//############   

		pos_mm  = 2;
		pos_nm1 = 1;
		P_mm = 1.0;
		R_m  = R;
		//do m = 1,n_harm 
		for( m = 1; m <=n_harm; m++ )
		{
			//  ,   m  1    
			//     
			P_nm   = P_mm;
			P_n1m  = 0.0;
			pos_nm = pos_mm;
			R_n = R_m;
			n1  = m+1;
			dw2 = 0.0;
			dw3 = 0.0;
			// do n = m,n_harm
			for( n = m; n <=n_harm; n++ )
			{
				//!  ,   m  m    
				PR = P_nm*R_n;
				dw  = PR*(h_egm96[pos_nm]*cosmL + h_egm96[pos_nm+1]*sinmL);
				w1  = w1 + ((double)(n1))*dw;
				dw2 = dw2 + dw;
				dw3 = dw3 + PR*(-h_egm96[pos_nm]*sinmL + h_egm96[pos_nm+1]*cosmL);
				w4  = w4 + PR*(h_egm96[pos_nm1]*cosm1L + h_egm96[pos_nm1+1]*sinm1L);
				R_n = R_n*R;
				tmp = P_n1m;
				P_n1m = P_nm;
				n21 = n+n1;
				P_nm = ( ((double)(n21))*x[2]*P_n1m - ((double)(n+m))*tmp)/((double)(n1-m));
				n1 = n1+1;
				pos_nm1 = pos_nm1 + n21;
				pos_nm = pos_nm + n21;
			}
			w2 = ((double)(m))*dw2+w2;
			w3 = ((double)(m))*dw3+w3;
			R_m = R_m*R;
			cosm1L = cosmL;
			sinm1L = sinmL;
			cosmL = cosm1L*cosL - sinm1L*sinL;
			sinmL = cosm1L*sinL + sinm1L*cosL;
			n21 = m+m+1;
			P_mm = ((double)(n21))*ro*P_mm;
			pos_nm1 = n21 + pos_mm;
			pos_mm = pos_nm1 + 2;
		}
		//!    m=0
		P_n1m = 1.0;
		P_nm = x[2];
		pos_mm = 1;
		R_n = R;
		w1 = w1*ro;
		//do n = 1,n_harm
		for( n = 1; n <=n_harm; n++ )
		{
			w1 = w1 + ((double)(n+1))*h_egm96[pos_mm]*R_n*P_nm;
			if (n != n_harm )
			{
				n1 = n+1;
				R_n = R_n*R;
				tmp = P_n1m;
				P_n1m = P_nm;
				n21 = n+n1;
				P_nm = ( ((double)(n21))*x[2]*P_n1m - ((double)(n))*tmp )/n1;
				pos_mm = pos_mm +n21;
			}
		}
		//!   
		tmp = -muE/rv/rv;
		w2 = w4*ro - w2*x[2];
		f_harm[2] = (w1*x[2] - w2*ro)*tmp;
		w2 = w2*x[2];
		f_harm[0] = (w2*cosL + w3*sinL + w1*x[0])*tmp;
		f_harm[1] = (w2*sinL - w3*cosL + w1*x[1])*tmp;
	}

